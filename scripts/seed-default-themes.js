/**
 * Cria os temas padrÃ£o em um banco de dados
 * @param {PrismaClient} prisma - InstÃ¢ncia do Prisma Client conectada ao banco do tenant
 */
async function seedDefaultThemes(prisma) {
  try {
    console.log("ðŸŽ¨ Criando temas padrÃ£o...\n");

    let created = 0;
    let skipped = 0;

    for (const theme of defaultThemes) {
      try {
        // Verificar se o tema jÃ¡ existe
        const existing = await prisma.theme.findUnique({
          where: { name: theme.name },
        });

        if (existing) {
          console.log(`   â­ï¸  Tema "${theme.name}" jÃ¡ existe, pulando...`);
          skipped++;
          continue;
        }

        // Criar tema
        await prisma.theme.create({
          data: {
            name: theme.name,
            description: theme.description,
            type: theme.type,
            isActive: theme.isActive,
            colors: theme.colors,
          },
        });

        console.log(`   âœ… Tema "${theme.name}" criado com sucesso`);
        created++;
      } catch (error) {
        console.error(`   âŒ Erro ao criar tema "${theme.name}":`, error.message);
      }
    }

    console.log("\n" + "=".repeat(50));
    console.log(`\nâœ… Temas padrÃ£o processados!`);
    console.log(`   ${created} temas criados`);
    console.log(`   ${skipped} temas jÃ¡ existentes`);
    console.log(`   ${defaultThemes.length} temas no total\n`);

    return { created, skipped };
  } catch (error) {
    console.error("âŒ Erro ao criar temas padrÃ£o:", error);
    throw error;
  }
}

